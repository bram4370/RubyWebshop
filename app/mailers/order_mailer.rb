class OrderMailer < ApplicationMailer
  default from: 'Rick Astley <Rick@roll.com>'

  # Subject can be set in your I18n file at config/locales/en.yml
  # with the following lookup:
  #
  #   en.order_mailer.received.subject
  #
  def received(order)
    @order = order

    mail to: order.email, subject: '7 facts you will never believe! 3 will shock you'
  end

  # Subject can be set in your I18n file at config/locales/en.yml
  # with the following lookup:
  #
  #   en.order_mailer.shipped.subject
  #
  def shipped(order)
    @order = order

    mail to: order.email, subject: '8 facts you will never believe! Doctors hate it!'
  end
end
